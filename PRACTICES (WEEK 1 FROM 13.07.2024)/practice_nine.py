# 1. Create a set of vowels.
# 2. Check if the letter "e" is in the set.
# 3. Check if the letter "x" is in the set.
# 4. Print the results.


vowels = {"a", "e", "i", "o", "u"}
print(vowels)

if "e" in vowels:
    print(" E is in the set of vowels.")
else:
    print("E is not in the set of vowels.")

if "x" in vowels:
    print("X is in the set of vowels.")
else:
    print("X is not in the set of vowels.")
